package br.com.paxtecnologia.pma.relatorio.vo2;

public class AcessoUsuarioVO {

	private Double acessoMes;
	private Double semAcesso;
	private Double totalUsuarios;

	public Double getAcessoMes() {
		return acessoMes;
	}

	public void setAcessoMes(Double acessoMes) {
		this.acessoMes = acessoMes;
	}

	public Double getSemAcesso() {
		return semAcesso;
	}

	public void setSemAcesso(Double semAcesso) {
		this.semAcesso = semAcesso;
	}

	public Double getTotalUsuarios() {
		return totalUsuarios;
	}

	public void setTotalUsuarios(Double totalUsuarios) {
		this.totalUsuarios = totalUsuarios;
	}

}
