package br.com.paxtecnologia.pma.relatorio.vo2;

public class KeyValue {

	private String key;
	private Double value;

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public Double getValue() {
		return value;
	}

	public void setValue(Double value) {
		this.value = value;
	}

}
